function getWordRoot(word) {
    if (word.length <= 4) {
        return word;
    }
    return word.slice(0, -2);
}

// –ü–æ–∏—Å–∫ –ø–æ –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º —Å —Ä–∞—Å—á–µ—Ç–æ–º —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç–∏
function findAnswerByKeywords(message) {
    const lowerMessage = message.toLowerCase();
    const cleanMessage = lowerMessage
        .replace(/[^\w\s–∞-—è—ë]/gi, " ")
        .replace(/\s+/g, " ")
        .trim();
    const messageWords = cleanMessage
        .split(" ")
        .filter((word) => word.length > 3);

    if (messageWords.length < 2) {
        console.log(`–°–æ–æ–±—â–µ–Ω–∏–µ "${message}" –Ω–µ —Å–æ–¥–µ—Ä–∂–∏—Ç –∑–Ω–∞—á–∏–º—ã—Ö —Å–ª–æ–≤`);
        return { answer: null, relevance: 0 };
    }

    let bestMatch = null;
    let maxMatches = 0;
    let maxRelevanceScore = 0;

    for (const item of knowledgeBase) {
        if (!item.keywords || item.keywords.length === 0 || !item.answer || item.answer.trim() === "") {
            continue;
        }

        let matchCount = 0;
        let relevanceScore = 0;
        const matchedKeywords = [];

        for (const keyword of item.keywords) {
            if (!keyword || keyword.trim() === "") continue;

            const cleanKeyword = keyword.toLowerCase().trim();
            let keywordMatched = false;
            let currentRelevance = 0;

            if (cleanMessage.includes(cleanKeyword)) {
                matchCount++;
                matchedKeywords.push(cleanKeyword);
                keywordMatched = true;
                currentRelevance = cleanKeyword.length * 3;
            } else {
                const keywordWords = cleanKeyword
                    .split(" ")
                    .filter((word) => word.length > 2);

                for (const keywordWord of keywordWords) {
                    if (keywordWord.length < 4) continue;

                    for (const messageWord of messageWords) {
                        if (keywordWord.length >= 4 && messageWord.length >= 4) {
                            if (messageWord.includes(keywordWord) || keywordWord.includes(messageWord)) {
                                if (!keywordMatched) {
                                    matchCount++;
                                    matchedKeywords.push(cleanKeyword);
                                    keywordMatched = true;
                                    currentRelevance = Math.min(keywordWord.length, messageWord.length) * 2;
                                }
                                break;
                            }
                        }

                        if (!keywordMatched && keywordWord.length > 4 && messageWord.length > 4) {
                            const keywordRoot = getWordRoot(keywordWord);
                            const messageRoot = getWordRoot(messageWord);

                            if (
                                keywordRoot.length >= 4 &&
                                messageRoot.length >= 4 &&
                                (keywordRoot === messageRoot || messageWord.includes(keywordRoot) || keywordWord.includes(messageRoot))
                            ) {
                                matchCount++;
                                matchedKeywords.push(cleanKeyword + " (–∫–æ—Ä–µ–Ω—å)");
                                keywordMatched = true;
                                currentRelevance = Math.min(keywordRoot.length, messageRoot.length) * 0.7;
                                break;
                            }
                        }
                    }
                    if (keywordMatched) break;
                }
            }

            relevanceScore += currentRelevance;
        }

        if (matchCount >= 1) {
            if (
                matchCount > maxMatches ||
                (matchCount === maxMatches && relevanceScore > maxRelevanceScore)
            ) {
                maxMatches = matchCount;
                maxRelevanceScore = relevanceScore;
                bestMatch = {
                    answer: item.answer,
                    matchedKeywords: matchedKeywords,
                    matchCount: matchCount,
                    relevanceScore: relevanceScore,
                };
            }
        }
    }

    if (bestMatch) {
        const basePercent = (bestMatch.matchCount / Math.max(1, messageWords.length)) * 33;
        const accuracyBonus = Math.min(20, bestMatch.relevanceScore / Math.max(1, bestMatch.matchCount));
        const relevancePercent = Math.min(100, basePercent + accuracyBonus);

        console.log(
            `–ù–∞–π–¥–µ–Ω–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ –ø–æ –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º –¥–ª—è "${message}" –ø–æ ${bestMatch.matchCount} –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º: ${bestMatch.matchedKeywords.join(", ")} (—Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç—å: ${bestMatch.relevanceScore}, –ø—Ä–æ—Ü–µ–Ω—Ç: ${relevancePercent.toFixed(1)}%)`
        );

        return {
            answer: bestMatch.answer,
            relevance: relevancePercent,
        };
    }

    console.log(`–ù–µ –Ω–∞–π–¥–µ–Ω–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π –ø–æ –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º –¥–ª—è —Å–æ–æ–±—â–µ–Ω–∏—è: "${message}"`);
    return { answer: null, relevance: 0 };
}

